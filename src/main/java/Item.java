public class Item {
    public String title;
    public String category;
    public String genre;
    public String format;
    public Integer year;

    public Item(String title, String category, String genre, String format, Integer year){
        this.title = title;
        this.category = category;
        this.genre = genre;
        this.format = format;
        this.year = year;
    }
}
